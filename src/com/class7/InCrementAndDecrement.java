package com.class7;

public class InCrementAndDecrement {
	public static void main (String [] shah) {
		
		int num = 10;
		System.out.println(num);
		
		num = num+1;
		System.out.println(num);
		num +=1;
		System.out.println(num);
		num++;
		System.out.println(num);
		System.out.println("------------------");
		
		num--;
		System.out.println(num);
		num-=1;
		System.out.println(num);
	}

}
