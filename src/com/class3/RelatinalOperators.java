package com.class3;

public class RelatinalOperators {

	public static void main(String[] args) {
		

		int a = 10;
		int b = 15;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		
		int x = 100;
		int y = 90;
		
		boolean result = x>y;
		System.out.println(result);
		
		System.out.println("-----------------------------------------");
		
		System.out.println(x==y);//compare
		System.out.println(x=y);//assiging value of y to variable x
		System.out.println(x);//90
		
		boolean result1 = x==y;
		System.out.println(result1);
		
		
		byte num = 10;
		byte num1 =10;
		
		boolean boo = num>=num1;
		
		System.out.println("The result boo is "+boo);
		
		
		

	}

}
